<div style="margin-left: 5%;">
<h4>Entry Fee for Katiki Waterfalls</h4><br>
<p>There is no entry fee to visit the Katiki falls. However, jeeps are available for visitors as an alternative to trekking to the falls. It takes about 10 people per ride at a cost of Rs. 250 to Rs. 300 per person.<br><br>
<img src="/vizagtourism/pictures/location icon.png.png" style="height:20px;width:20px">&nbsp <b>Location:</b> Katiki Waterfalls Rd, Visakhapatnam, Andhra Pradesh.<br><br>
<img src="/vizagtourism/pictures/watch.png" style="height:20px;width:20px">&nbsp <b>Timings</b>
Katiki falls can be visited from 6:00 in the morning to 7:00 in the evening on all days of the week.</p><br>
<img src="/vizagtourism/pictures/transport.png" style="width:20px;height:20px" >&nbsp <b>Transportation Details:</b>You can hire a cab from top car rental companies in Vizag and travel to Katiki village. From here you need to either trek up to the falls or take a Jeep ride.
